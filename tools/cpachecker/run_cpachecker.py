CWE_TYPE_TO_SPEC = {
    "CWE_401": "memorysafety-memtrack",
    "CWE_415": "memorysafety-free",
    "CWE_416": "memorysafety-deref",
    "CWE_UNKNOWN": "memorysafety",
}


def parse_i_file_content_from_framac_out_file(framac_out_path: str, i_file_path: str):
    with open(framac_out_path, "r") as infile, open(i_file_path, "w") as outfile:
        found = False
        for line in infile:
            if "/* Generated by Frama-C */" in line:
                found = True
            if found:
                outfile.write(line)


def group_c_files_from_dir(src_dir: str) -> dict:
    groups = defaultdict(list)

    for root, _, files in os.walk(src_dir):
        for file_name in files:
            file_path = os.path.join(root, file_name)
            if os.path.isfile(file_path) and file_name.endswith(".c"):
                match = re.match(r"(.+_\d+)[a-zA-Z]?\.c", file_name)
                if match:
                    group_label = match.group(1)
                    groups[(group_label, root)].append(file_path)

    return dict(groups)


def get_cwe_type_from_src_dir(src_dir):
    cwe_types = ["CWE_UNKNOWN", "CWE_401", "CWE_415", "CWE_416"]
    for cwe_type in cwe_types:
        if cwe_type in src_dir:
            return cwe_type

    return cwe_types[0]


def preprocess(src_dir: str, res_dir: str):
    print("Generating preprocessed C files...")
    include_dir = os.path.join(os.path.dirname(os.path.dirname(src_dir)), "testcasesupport")
    include_file_1 = os.path.join(include_dir, "io.c")
    include_file_2 = os.path.join(include_dir, "std_thread.c")

    groups = group_c_files_from_dir(src_dir)
    frama_c = "/opt/opam/default/bin/frama-c"

    for group, file_names in groups.items():
        file_names.append(include_file_1)
        file_names.append(include_file_2)

        group_label, group_path = group
        temp_out_file = os.path.join(res_dir, "temp_out")

        preprocess_cmd = (f"{frama_c} -machdep gcc_x86_64 -cpp-command 'gcc -E -DOMITGOOD -DINCLUDEMAIN=ON -I {include_dir} -o %o %i' "
                          f"-print {' '.join(file_names)} > {temp_out_file}")
        run_cmd(preprocess_cmd, res_dir)
        i_file_path = os.path.join(res_dir, f"{group_label}_bad.i")
        parse_i_file_content_from_framac_out_file(temp_out_file, i_file_path)

        preprocess_cmd = (f"{frama_c} -machdep gcc_x86_64 -cpp-command 'gcc -E -DOMITBAD -DINCLUDEMAIN=ON -I {include_dir} -o %o %i' "
                          f"-print {' '.join(file_names)} > {temp_out_file}")
        run_cmd(preprocess_cmd, res_dir)
        i_file_path = os.path.join(res_dir, f"{group_label}_good.i")
        parse_i_file_content_from_framac_out_file(temp_out_file, i_file_path)


def analyze(tool: str, specification: str, res_dir: str):
    print("Starting analysis...")
    for file in os.listdir(res_dir):
        file_path = os.path.join(res_dir, file)
        if file.endswith(".i") and os.path.isfile(file_path):
            flags = (f"--spec {specification} --disable-java-assertions --smg "
                     f"--option analysis.stopAfterError=false --output-path {file[0:-2]}")
            command = f"{tool} {flags} {file}"
            print(f"Analyzing {file}")
            try:
                run_cmd(command, res_dir)
            except RuntimeError:
                print("Crashed! Continuing...")


class CpacheckerRunner(ToolRunner):
    DEFAULT_PATH = os.path.join("tools", "cpachecker", "cpachecker", "bin", "cpachecker")
    SUPPORTED_CWE_TYPES = ["memory_leak", "double_free", "use_after_free"]
    SUPPORTED_LANGUAGES = ["C"]

    def execute(self, src_dir: str, res_dir: str, build: str, language: str):
        if build == Build.CMAKE or build == Build.CONF.value:
            print(f"Invalid test suite configuration: {build}. Skipping analysis for the given test suite.")
            print("Hint: Build and configure types are not supported by this tool")
            print("The only options it supports: raw files or preprocessed files")
            return

        preprocess(src_dir, res_dir)
        cwe_type = get_cwe_type_from_src_dir(src_dir)
        specification = CWE_TYPE_TO_SPEC[cwe_type]
        analyze(self.tool_path, specification, res_dir)